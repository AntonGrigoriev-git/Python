# Calculating the sum and product of numbers

first_number = int(input("Enter the first number: "))
second_number = int(input("Enter the second number: "))
third_number = int(input("Enter the third number: "))

sum_of_numbers = first_number + second_number + third_number
product_of_numbers = first_number * second_number * third_number

print(f"The sum of the numbers is equal to {sum_of_numbers}")
print(f"The product of the numbers is equal to {product_of_numbers}")